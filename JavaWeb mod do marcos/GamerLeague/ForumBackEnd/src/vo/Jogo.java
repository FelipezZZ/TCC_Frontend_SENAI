package vo;

public class Jogo{
	
	private int cod_jogo;
	private String jogo;
	
	public Jogo(int cod_jogo, String jogo) {
		
	}
	
	public Jogo() {
		
	}
	
	public int getCod_jogo() {
		return cod_jogo;
	}
	public void setCod_jogo(int cod_jogo) {
		this.cod_jogo = cod_jogo;
	}
	public String getJogo() {
		return jogo;
	}
	public void setJogo(String jogo) {
		this.jogo = jogo;
	}
}
